#include "ft_printf.h"

int ft_size_hex(unsigned long n)
{
	int k;

	k = 0;
	if(n < 0)
	{
		n = -n;
		k++;
	}
	if(n == 0)
		k++;
	while(n > 0)
	{
		n = n / 16;
		k++;
	}
	return(k);
}
